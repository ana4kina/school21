#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*c;

	i = 0;
	if (!s || (!s && !f))
		return (ft_strdup(""));
	else if (!f)
		return (ft_strdup(s));
	c = ft_strdup(s);
	if (!c)
		c = NULL;
	while (s[i])
	{
		c[i] = (*f)(i, s[i]);
		i++;
	}
	return (c);
}
